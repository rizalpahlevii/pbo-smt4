public class Magazine extends Print {
    Integer issn, issue;

    public Integer getIssn() {
        return issn;
    }

    public Integer getIssue() {
        return issue;
    }

    public void setIssn(Integer issn) {
        this.issn = issn;
    }

    public void setIssue(Integer issue) {
        this.issue = issue;
    }
}
