public class Matematika {
    void pertambahan(int param1, int param2) {
        System.out.println("Pertambahan : " + param1 + " + " + param2 + " = " + (param1 + param2));
    }

    void pengurangan(int param1, int param2) {
        System.out.println("Pengurangan : " + param1 + " - " + param2 + " = " + (param1 - param2));
    }

    void perkalian(int param1, int param2) {
        System.out.println("Perkalian : " + param1 + " x " + param2 + " = " + (param1 * param2));
    }

    void pembagian(int param1, int param2) {
        System.out.println("Pembagian : " + param1 + " / " + param2 + " = " + (param1 / param2));
    }

}